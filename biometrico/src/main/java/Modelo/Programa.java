package Modelo;

public class Programa {
    private String idPrograma;
    private String nombrePrograma;

    // Getters y setters
    public String getIdPrograma() { return idPrograma; }
    public void setIdPrograma(String idPrograma) { this.idPrograma = idPrograma; }

    public String getNombrePrograma() { return nombrePrograma; }
    public void setNombrePrograma(String nombrePrograma) { this.nombrePrograma = nombrePrograma; }
}
